func (a *Adaptor) Init(info *relaycommon.RelayInfo) {
}

func (a *Adaptor) GetRequestURL(info *relaycommon.RelayInfo) (string, error) {
	// For passthrough, the URL is built from the original request path
	// This will be handled by the router directly
	return fmt.Sprintf("%s%s", info.ChannelBaseUrl, info.RequestURLPath), nil
}

func (a *Adaptor) SetupRequestHeader(c *gin.Context, req *http.Header, info *relaycommon.RelayInfo) error {
	channel.SetupApiRequestHeader(info, c, req)
	// Semantic Scholar uses x-api-key header for authentication
	req.Set("x-api-key", info.ApiKey)
	return nil
}

